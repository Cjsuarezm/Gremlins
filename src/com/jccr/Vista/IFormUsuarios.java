public final void cargarTablaUsuarios(){
    this.getmyControlador().cargarUsuarios("");
    Object[] NombreColumnas = {"ID USUARIO", "NOMBRE", "IDENTIFICACION", "ESTADO"};
    this.dtm_Listado = new DefaultTableModel(NombreColumnas, 0){
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };
    this.tablaListadoUauarios.setModel(this.dtm_Listado);
    this.tablaListadoUauarios.getColumnModel().getColumn(0).setPreferredWidth(30);
    this.tablaListadoUauarios.getColumnModel().getColumn(1).setPreferredWidth(100);
    this.tablaListadoUauarios.getColumnModel().getColumn(2).setPreferredWidth(60);
    this.tablaListadoUauarios.getColumnModel().getColumn(3).setPreferredWidth(60);
    this.tablaListadoUauarios.getColumnModel().getColumn(0).setCellRenderer(this.modeloCentrar);
    this.tablaListadoUauarios.getColumnModel().getColumn(1).setCellRenderer(this.modeloCentrar);
    //TableRowSorter<TableModel> tablaOrdenada = new TableRowSorter<TableModel>(this.dtm_Listado);
    //this.tablaListadoUauarios.setRowSorter(tablaOrdenada);
    
    int x = this.getmyControlador().getMyUsuario().size();
    for(int i = 0; i < x; i++){
        String estado = "ACTIVO";
        int idUsu = this.getmyControlador().getMyUsuario().get(i).getUsuId();
        String nomUsu = this.getmyControlador().getMyUsuario().get(i).getUsuNom();
        String usuIdent = this.getmyControlador().getMyUsuario().get(i).getUsuIdent();
        int estUsu = this.getmyControlador().getMyUsuario().get(i).getUsuEstado();
        if(estUsu == 0){
            estado = "INACTIVO";
        }
            Object[] nuevaFila = {idUsu, nomUsu, usuIdent, estado};
            this.dtm_Listado.addRow(nuevaFila);
    }
    this.getmyControlador().getMyUsuario().clear();
}


